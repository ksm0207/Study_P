# 숫자 자료형

#  정수형 말 그대로 정수를 뜻하는 자료형 입니다

number = 1
number2 = 10
number3 = -100
number4 = -1000

print(number, number2, number3, number4)

# 실수형 --> 소수점이 포함된 숫자를 뜻합니다
float1 = 1.2
float2 = -1.1
float3 = 1.222211
float4 = -1.222211

print(float1, float2, float3, float4)


# 컴퓨터식 지수 표현 방식
a = 4.24e10
b = 4.24e-10

print(a, b)

# 사칙연산
a = 3
b = 4
c = a + b
print(c)

a = 10
b = 14
c = a * b
print(c)

a = 50
b = 25
c = a / b
print(c)

# x 의 y 제곱을 나타내는 ** 연산자
a = 3
b = 4
c = a ** b
print(c)

# 나눗셈 후 나머지를 반환하는 % 연산자

a = 7
b = 3
c = a % b
print(c)

# 나눗셈 후 몫을 반환하는 // 연산자

a = 7
b = 4
c = a // b
print(c)

